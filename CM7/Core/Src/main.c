/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */
/* USER CODE BEGIN Boot_Mode_Sequence_0 */
#if defined(DUAL_CORE_BOOT_SYNC_SEQUENCE)
  int32_t timeout;
#endif /* DUAL_CORE_BOOT_SYNC_SEQUENCE */
/* USER CODE END Boot_Mode_Sequence_0 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

/* USER CODE BEGIN Boot_Mode_Sequence_1 */
#if defined(DUAL_CORE_BOOT_SYNC_SEQUENCE)
  /* Wait until CPU2 boots and enters in stop mode or timeout*/
  timeout = 0xFFFF;
  while((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) != RESET) && (timeout-- > 0));
  if ( timeout < 0 )
  {
  //Error_Handler();
  }
#endif /* DUAL_CORE_BOOT_SYNC_SEQUENCE */
/* USER CODE END Boot_Mode_Sequence_1 */
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();
/* USER CODE BEGIN Boot_Mode_Sequence_2 */
#if defined(DUAL_CORE_BOOT_SYNC_SEQUENCE)
/* When system initialization is finished, Cortex-M7 will release Cortex-M4 by means of
HSEM notification */
/*HW semaphore Clock enable*/
__HAL_RCC_HSEM_CLK_ENABLE();
/*Take HSEM */
HAL_HSEM_FastTake(HSEM_ID_0);
/*Release HSEM in order to notify the CPU2(CM4)*/
HAL_HSEM_Release(HSEM_ID_0,0);
/* wait until CPU2 wakes up from stop mode */
timeout = 0xFFFF;
while((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) == RESET) && (timeout-- > 0));
if ( timeout < 0 )
{
//Error_Handler();
}
#endif /* DUAL_CORE_BOOT_SYNC_SEQUENCE */
/* USER CODE END Boot_Mode_Sequence_2 */

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_UART7_Init();
  MX_UART4_Init();
  MX_UART8_Init();
  /* USER CODE BEGIN 2 */
  /* Power rails are controlled explicitly in main. */
  PowerControl_SetGNSS(true);
  PowerControl_SetIMU(true);
  PowerControl_SetIridium(true);
  HAL_Delay(1000);

  IMU_Init(&huart4);

  Iridium_Init(&huart7);
  GNSS_Init(&huart8);
  SSR_Init();
  (void)SSR_SetMode(SSR_CH0, SSR_MODE_BLINK_500MS);
  (void)SSR_SetMode(SSR_CH1, SSR_MODE_BLINK_500MS);
  (void)SSR_SetMode(SSR_CH2, SSR_MODE_BLINK_500MS);
  (void)SSR_SetMode(SSR_CH3, SSR_MODE_BLINK_500MS);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    IMU_Data_t imu_data;
    const uint8_t *iridium_rx_data = NULL;
    GNSS_BestPosA_t gnss_bestposa;

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    if (IMU_PollNewData(&imu_data))
    {
      /* New NAV data received: use imu_data here. */
      (void)imu_data;
    }

    if (Iridium_PollNewData(&iridium_rx_data))
    {
      uint16_t iridium_rx_len = Iridium_GetRxLength();
      /* Inspect iridium_rx_data in debugger (example: "AT\r\r\nOK\r\n"). */
      (void)iridium_rx_data;
      (void)iridium_rx_len;
    }

    uint8_t ii = 0;
    if (GNSS_Process(&gnss_bestposa))
    {
      /* New BESTPOSA parsed data received: use gnss_bestposa here. */
      (void)gnss_bestposa;
      ii = 1;
    }

    SSR_Process();

  }
  /* USER CODE END 3 */
}

